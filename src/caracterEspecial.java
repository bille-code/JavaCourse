public class caracterEspecial {
    
    public static void main(String[] args) {
        
        String nombre = "Rubén";
        
        System.out.println("Nueva linea \n" + nombre); // imprime la variable en una nueva linea
        System.out.println("Tabulador \t" + nombre); // Imprime el caracter de tabular 
        System.out.println("Retroceso: \b\b" + nombre); // Imprime un retroceso en la cadena
        System.out.println("Comillas simples \'" + nombre + "\'"); //Imprime comillas simples sobre la variable nombre
        System.out.println("Comillas Dobles \"" + nombre + "\" "); // Imprime comillas dobles en la concatenación de la variable
        
    }
    
}
